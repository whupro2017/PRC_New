package com.whu.pro.mapper.result;

public class CaseQueryResult {
    private int case_id;
    private String case_time;
    private String case_location;
    private String case_province;
    private String case_city;
    private String case_desc;
    private String case_type;

    public int getCase_id() {
        return case_id;
    }

    public void setCase_id(int case_id) {
        this.case_id = case_id;
    }

    public String getCase_time() {
        return case_time;
    }

    public void setCase_time(String case_time) {
        this.case_time = case_time;
    }

    public String getCase_location() {
        return case_location;
    }

    public void setCase_location(String case_location) {
        this.case_location = case_location;
    }

    public String getCase_province() {
        return case_province;
    }

    public void setCase_province(String case_province) {
        this.case_province = case_province;
    }

    public String getCase_city() {
        return case_city;
    }

    public void setCase_city(String case_city) {
        this.case_city = case_city;
    }

    public String getCase_desc() {
        return case_desc;
    }

    public void setCase_desc(String case_desc) {
        this.case_desc = case_desc;
    }

    public String getCase_type() {
        return case_type;
    }

    public void setCase_type(String case_type) {
        this.case_type = case_type;
    }

}
